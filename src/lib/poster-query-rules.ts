/**
 * 召回精确率闸门:该图解在当前 query 下是否应被召回。
 * - 无 query 或该 id 无规则 → 放行(向后兼容)
 * - 命中任一 negativeKeyword → 弃权(意图相反/不同)
 * - 无任一 keyword 命中 → 弃权(不相干就不召回,精确率优先)
 */
export function posterPassesQueryGate(
  id: string,
  query: string | null | undefined,
): boolean {
  if (!query) return true;
  const rule = POSTER_QUERY_RULES[id];
  if (!rule) return true;
  if (rule.negativeKeywords.some((w) => w && query.includes(w))) return false;
  return rule.keywords.some((w) => w && query.includes(w));
}
